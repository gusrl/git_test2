package di;

public class Anonymouce {
	private String name;
	private String age;
	private String engin;
	private String gender;
	public Anonymouce(String name, String age, String engin, String gender) {
		
		this.name = name;
		this.age = age;
		this.engin = engin;
		this.gender = gender;
	}
	public String getName() {
		return name;
	}
 
	public String getAge() {
		return age;
	}
	 
	public String getEngin() {
		return engin;
	}
	 
	public String getGender() {
		return gender;
	}
	 
	
	
}
